<?php

namespace App\Domain\Admin;

class AdminFilter
{
    /**
     * @var string
     */
    private $name = '';

    /**
     * @return string
     */
    public function name()
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }
}